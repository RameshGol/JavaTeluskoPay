class DemoDataTypes {
    public static void main(String args[]) {
        
        //DataTypes
        int num1 = 2; 
        int num2 = 3;
        int result = num1 + num2;
        System.out.println("The result is: " + result);

        byte by = 127;
        short sh = 558;
        long l = 5854l;
        
        float fl = 5.6f;
        double dl = 5.6;
        System.out.println("fl:"+ fl +" "+"dl:"+ dl);

        char c = 'k';
        boolean b = true;
        System.out.println("c:" + c);
        System.out.println("***Done***");

        char ch = 063;
        System.out.println("ch: " + ch); 
    }
} 
